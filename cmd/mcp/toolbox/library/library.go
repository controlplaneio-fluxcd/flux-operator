package library

import (
	"bytes"
	_ "embed"
	"encoding/gob"
	"fmt"
	"strings"
)

//go:embed index.db
var embeddedDatabase []byte

var searchIndexes map[IndexFormat]*SearchIndex

// DocumentMetadata holds the metadata for a Flux document.
type DocumentMetadata struct {
	Title    string   `json:"title,omitempty"`
	URL      string   `json:"url"`
	Group    string   `json:"group"`
	Kind     string   `json:"kind"`
	Keywords []string `json:"keywords"`
}

// ID returns a stable unique document identifier,
// derived from the group and kind for API docs or from the title slug.
func (d DocumentMetadata) ID() string {
	if d.Group != "" && d.Kind != "" {
		return fmt.Sprintf("%s-%s", d.Group, d.Kind)
	}
	return strings.ToLower(strings.NewReplacer(" ", "-", "/", "-").Replace(d.Title))
}

// Label returns a human-readable document title.
func (d DocumentMetadata) Label() string {
	if d.Title != "" {
		return d.Title
	}
	return fmt.Sprintf("%s (%s)", d.Kind, d.Group)
}

// Library holds a collection of document references represented by the DocumentMetadata type.
// It provides methods to search in the metadata and fetch the document content.
type Library struct {
	Documents []DocumentMetadata `json:"documents"`
}

func init() {
	db, err := loadDatabase(embeddedDatabase)
	if err != nil {
		// If the database doesn't exist yet (e.g., during first build), that's okay
		// The database will be generated by the indexer tool
		return
	}
	searchIndexes = db.Indexes
}

// loadDatabase deserializes the embedded search database.
func loadDatabase(data []byte) (*SearchDatabase, error) {
	if len(data) == 0 {
		return nil, fmt.Errorf("embedded database is empty")
	}

	reader := bytes.NewReader(data)
	decoder := gob.NewDecoder(reader)

	var db SearchDatabase
	if err := decoder.Decode(&db); err != nil {
		return nil, fmt.Errorf("failed to decode database: %w", err)
	}

	return &db, nil
}

// GetSearchIndex returns the loaded search index for the given format,
// or nil if the format is unknown or the index is not available.
func GetSearchIndex(format IndexFormat) *SearchIndex {
	return searchIndexes[format]
}
